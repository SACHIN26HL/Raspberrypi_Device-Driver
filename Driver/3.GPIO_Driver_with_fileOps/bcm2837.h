#define GPIO_PIN_OUTPUY_SET1 0x7E200020

#define GPIO_PIN_OUTPUY_CLR0 0x7E200028
#define GPIO_PIN_OUTPUY_CLR1 0x7E20002C

#define GPIO_PIN_LEVEL0 0x7E200034
#define GPIO_PIN_LEVEL1 0x7E200038



#endif
